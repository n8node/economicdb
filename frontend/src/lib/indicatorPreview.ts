export type IndicatorDetailExplanation = {
  lead: string;
  paragraphs: string[];
};

function nameDetail(name: string): { about?: string; why?: string } | null {
  const lower = name.toLowerCase();
  if (lower.includes("inflation") || lower.includes("cpi") || lower.includes("инфляц")) {
    return {
      about: "Индикатор измеряет темп роста цен и помогает отделить разовые ценовые шоки от устойчивой инфляционной динамики.",
      why: "Центральные банки и рынки используют инфляцию для оценки необходимости ужесточения или смягчения политики.",
    };
  }
  if (lower.includes("unemployment") || lower.includes("безработ")) {
    return {
      about: "Показывает долю рабочей силы без работы при активном поиске занятости.",
      why: "Рост безработицы обычно сопровождает экономическое замедление и ослабление потребительского спроса.",
    };
  }
  if (lower.includes("gdp") || lower.includes("ввп")) {
    return {
      about: "Отражает совокупный объём экономической активности и её темпы изменения.",
      why: "Сравнение с потенциальным ростом и динамикой компонентов ВВП помогает понять фазу цикла.",
    };
  }
  if (lower.includes("yield") || lower.includes("доходност") || lower.includes("spread") || lower.includes("спред")) {
    return {
      about: "Показатель фиксирует доходность или премию за риск на долговом/кредитном рынке.",
      why: "Рост доходности и спредов часто означает более жёсткие финансовые условия и повышенную осторожность инвесторов.",
    };
  }
  if (lower.includes("retail") || lower.includes("рознич")) {
    return {
      about: "Отражает объём или динамику розничных продаж и потребительского спроса.",
      why: "Слабые продажи — ранний сигнал охлаждения потребления, одного из ключевых драйверов роста.",
    };
  }
  return null;
}

function buildContextParagraph(
  item: Pick<IndicatorListItem, "unit" | "frequency" | "country">,
  countryLabel: string,
  sourceLabel: string,
): string {
  const freq = FREQ_PUBLISH[item.frequency] || FREQ_LABELS[item.frequency]?.toLowerCase() || item.frequency;
  const unit = item.unit ? ` Значения приводятся в ${item.unit}.` : "";
  return `География: ${countryLabel}. Обновление: ${freq}, источник — ${sourceLabel}.${unit}`;
}

function fxDescription(name: string): string | null {
  const pair = name.match(/^([A-Z]{3})\s*\/\s*([A-Z]{3})$/i);
  if (!pair) return null;
  const [, base, quote] = pair;
  return `Курс ${base.toUpperCase()} к ${quote.toUpperCase()}: сколько единиц ${quote.toUpperCase()} стоит одна единица ${base.toUpperCase()}.`;
}

export function buildIndicatorDetailExplanation(
  item: Pick<IndicatorListItem, "category" | "unit" | "frequency" | "name_ru" | "country">,
  options?: { categoryLabel?: string; countryLabel?: string; sourceLabel?: string },
): IndicatorDetailExplanation {
  const lead = buildIndicatorDescription(item, options?.categoryLabel);
  const named = nameDetail(item.name_ru);
  const category = CATEGORY_DETAIL[item.category];
  const paragraphs: string[] = [];

  const detailAbout = named?.about || category?.about;
  if (detailAbout) paragraphs.push(detailAbout);

  const why = named?.why || category?.why;
  if (why) paragraphs.push(why);

  paragraphs.push(
    buildContextParagraph(
      item,
      options?.countryLabel || item.country.toUpperCase(),
      options?.sourceLabel || item.country.toUpperCase(),
    ),
  );

  if (paragraphs.length === 1) {
    paragraphs.unshift(lead);
  }

  return { lead, paragraphs };
}

export function buildIndicatorDescription(
  item: Pick<IndicatorListItem, "category" | "unit" | "frequency" | "name_ru" | "country">,
  categoryLabel?: string,
): string {
  if (item.category === "fx") {
    const fxHint = fxDescription(item.name_ru);
    if (fxHint) return fxHint;
  }

  const hint = CATEGORY_HINTS[item.category];
  if (hint) return hint;

  const label = categoryLabel || item.category;
  const freq = FREQ_LABELS[item.frequency] || item.frequency;
  const unit = item.unit ? ` Единица измерения: ${item.unit}.` : "";
  return `Макроэкономический показатель категории «${label}». Обновляется с частотой ${freq}.${unit}`;
}
